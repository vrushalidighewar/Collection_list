package com.app.model;

import java.util.List;

public class Product {
         private int id;
         private String name;
         private List colors;
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public List getColors() {
			return colors;
		}
		public void setColors(List colors) {
			this.colors = colors;
		}
         
         
}
